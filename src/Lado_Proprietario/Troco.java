package Lado_Proprietario;

public class Troco {
	
	private int dezR, cincoR, doisR, umR, cinquentaC;

	public Troco(int dezR, int cincoR, int doisR, int umR, int cinquentaC) {
		super();
		this.dezR = dezR;
		this.cincoR = cincoR;
		this.doisR = doisR;
		this.umR = umR;
		this.cinquentaC = cinquentaC;
	}

	public int getDezR() {
		return dezR;
	}

	public void setDezR(int dezR) {
		this.dezR = dezR;
	}

	public int getCincoR() {
		return cincoR;
	}

	public void setCincoR(int cincoR) {
		this.cincoR = cincoR;
	}

	public int getDoisR() {
		return doisR;
	}

	public void setDoisR(int doisR) {
		this.doisR = doisR;
	}

	public int getUmR() {
		return umR;
	}

	public void setUmR(int umR) {
		this.umR = umR;
	}

	public int getCinquentaC() {
		return cinquentaC;
	}

	public void setCinquentaC(int cinquentaC) {
		this.cinquentaC = cinquentaC;
	}
	
	
	

}
